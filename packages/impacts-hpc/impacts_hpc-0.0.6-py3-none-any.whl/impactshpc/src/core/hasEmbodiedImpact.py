from abc import ABC, abstractmethod

from impactshpc.src.core.ReplicableValue import SourcedValue
from impactshpc.src.core.impacts import Impacts


class HasEmbodiedImpacts(ABC):
    def __init__(self, embodied_impacts: Impacts | None = None) -> None:
        self.embodied_impacts = embodied_impacts

    @abstractmethod
    def estimate_embodied_impacts(self) -> Impacts:
        pass
